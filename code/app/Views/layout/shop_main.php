<script type="text/javascript">
	$(document).ready(function () {

		$('.asidePupupCloseLink').on('click', function () {

			$('#spopup').hide('slow');
			return false;
		});



		function calculateCartTotal() {
			var allRowsTotal = 0;
			var eachRowTotal = 0;

			$('input[id^=cartQuantity]').each(function () {

				var cartIndex = $(this).attr('lang');

				var quantity = parseInt($('#cartQuantity' + cartIndex).val());

				var finalPrice = parseFloat($('#finalPrice' + cartIndex).text());

				eachRowTotal = finalPrice * quantity;

				allRowsTotal += eachRowTotal;

			});

			$('#shoppingCartTotalSpan').text(allRowsTotal);

		}

		$(document).on("change keyup", "input[id^=cartQuantity]", function () {
			var cartIndex = $(this).attr('lang');
			var quantity = parseInt($(this).val());

			if (isNaN(quantity)) {
				quantity = 1;
				$('#cartQuantity' + cartIndex).val('1')
			}

			//alert(cartIndex + 'k'+ quantity);
			calculateCartTotal();
			//shoppingCartUpdate(cartIndex, quantity);
		});

		$(document).on("click", ".plus, .minus", function () {
			var cartIndex = $(this).attr('lang');
			var quantity = parseInt($('#cartQuantity' + cartIndex).val());

			if (isNaN(quantity)) {
				quantity = 1;
				$('#cartQuantity' + cartIndex).val('1')
			}
			//alert(cartIndex + 'k'+ quantity);
			calculateCartTotal();
			//shoppingCartUpdate(cartIndex, quantity);
		});

	});


</script>





